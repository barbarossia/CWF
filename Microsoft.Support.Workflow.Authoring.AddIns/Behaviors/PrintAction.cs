
namespace Microsoft.Support.Workflow.Authoring.Behaviors
{
    /// <summary>
    /// User print workflow actions
    /// </summary>
    public enum PrintAction
    {
        /// <summary>
        /// User not want to print
        /// </summary>
        NoneAction,
        /// <summary>
        /// User want to print all workflow
        /// </summary>
        PrintAll,
        /// <summary>
        /// User want to print which he/she selected
        /// </summary>
        PrintUserSelection,
    }
}
